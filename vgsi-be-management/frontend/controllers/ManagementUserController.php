    /**
     * @SWG\Get(
     *      path="/management-user/detail?id={id}",
     *      operationId="ManagementUser detail",
     *      summary="ManagementUser",
     *      consumes = {"application/json"},
     *      produces = {"application/json"},
     *      tags={"ManagementUser"},
     *      @SWG\Parameter(in="header", name="X-Luci-Language", required=false, type="string", default="vi-VN", description="Language, format vi-VN, en-US"),
     *      @SWG\Parameter( in="query", name="id", type="integer", default="1", description="id ManagementUser" ),
     *      @SWG\Response(response=200, description="Info",
     *          @SWG\Schema(type="object",
     *              @SWG\Property(property="success", type="boolean"),
     *              @SWG\Property(property="statusCode", type="integer", default=200),
     *              @SWG\Property(property="data",  type="object", ref="#/definitions/ManagementUserResponse"),
     *          ),
     *      ),
     *      security = {
     *         {
     *             "api_app_key": {},
     *             "domain_origin": {},
     *              "http_bearer_auth": {}
     *         }
     *      },
     * )
     */
    public function actionDetail()
    {
        $user = Yii::$app->user->getIdentity();
        $id = Yii::$app->request->get("id", 0);
        if(is_numeric($id)){ $id = (int)$id;}else{ $id = 0;}
        $post = ManagementUserResponse::find()
                ->where([
                    'id' => $id,
                    'building_cluster_id' => $user->building_cluster_id,
                ])
                ->andWhere(['OR',
                    ['role_type' => ManagementUser::DEFAULT_ADMIN],
                    ['role_type' => ManagementUser::SUPPER_ADMIN],
                ])
                ->one();
        return $post;
    }

