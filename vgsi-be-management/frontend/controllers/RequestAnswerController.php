    /**
     * @SWG\Get(
     *      path="/request-answer/list?request_id={request_id}",
     *      operationId="RequestAnswer list",
     *      summary="RequestAnswer list",
     *      consumes = {"application/json"},
     *      produces = {"application/json"},
     *      tags={"RequestAnswer"},
     *      @SWG\Parameter(in="header", name="X-Luci-Language", required=false, type="string", default="vi-VN", description="Language, format vi-VN, en-US"),
     *      @SWG\Parameter(in="query", name="request_id", type="integer", default="", description="request id"),
     *      @SWG\Parameter(in="query", name="resident_user_id", type="integer", default="", description="resident user id"),
     *      @SWG\Parameter(in="query", name="management_user_id", type="integer", default="", description="management user id"),
     *      @SWG\Parameter(in="query", name="pageSize", type="integer", default=50, description="Per page/page"),
     *      @SWG\Parameter(in="query", name="page", type="integer", default=1, description="Current Page"),
     *      @SWG\Parameter(in="query", name="sort", type="string", default="", description="Sort by:<br/> <b>+-name</b>: Tên RequestAnswer <br/><b>+-status</b>: Trạng thái"),
     *      @SWG\Response(response=200, description="Info",
     *          @SWG\Schema(type="object",
     *              @SWG\Property(property="success", type="boolean"),
     *              @SWG\Property(property="statusCode", type="integer", default=200),
     *              @SWG\Property(property="data", type="object",
     *                  @SWG\Property(property="items", type="array",
     *                      @SWG\Items(type="object", ref="#/definitions/RequestAnswerResponse"),
     *                  ),
     *                  @SWG\Property(property="pagination", type="object", ref="#/definitions/Pagination"),
     *              ),
     *          ),
     *      ),
     *      security = {
     *         {
     *             "api_app_key": {},
     *             "domain_origin": {},
     *             "http_bearer_auth": {}
     *         }
     *      },
     * )
     */
    public function actionList()
    {
        $user = Yii::$app->user->getIdentity();
        $request_id = Yii::$app->request->get("request_id", 0);
        $resident_user_id = Yii::$app->request->get("resident_user_id", 0);
        $management_user_id = Yii::$app->request->get("management_user_id", 0);
        if(empty($request_id)){
            return [
                'success' => false,
                'message' => Yii::t('frontend', "Invalid data"),
                'statusCode' => ErrorCode::ERROR_INVALID_PARAM
            ];
        }
        $requestAnswers = RequestAnswerResponse::find()->with(['residentUser', 'managementUser'])->where(['request_id' => (int)$request_id, 'is_deleted' => RequestAnswer::NOT_DELETED]);
        if(!empty($resident_user_id)){
            $requestAnswers->andWhere(['resident_user_id' => (int)$resident_user_id]);
        }
        if(!empty($management_user_id)){
            $requestAnswers->andWhere(['management_user_id' => (int)$management_user_id]);
        }
        $requestAnswers = $requestAnswers->all();
        return [
            'items' => $requestAnswers,
        ];
    }

