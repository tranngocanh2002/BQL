     *             "api_app_key": {},
     *             "domain_origin": {},
     *              "http_bearer_auth": {}
     *         }
     *      },
     * )
     */
    public function actionDetail($id)
    {
        $user = Yii::$app->user->getIdentity();
        if(is_numeric($id)){ $id = (int)$id;}else{ $id = 0;}
        $post = ServiceProviderResponse::findOne(['id' => $id, 'building_cluster_id' => $user->building_cluster_id, 'is_deleted' => ServiceProvider::NOT_DELETED]);
        return $post;
    }


